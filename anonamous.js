// JavaScript Anonymous Functions

// DEFINATION : 
// Anonymous functions are function expressions that do not have a name associated with them. 
// They are also sometimes referred to as lambda functions or function expressions.

// Anonymous functions are typically used when the same code needs to be executed only once or twice throughout a program. 
// They can be assigned to variables or passed as parameters to other functions.

var anonfun = function(){
    console.log('ANONA');
}

anonfun();

