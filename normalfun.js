// JavaScript Named Functions

// DEFINATION : 
// Named function: Using the function keyword followed by a name 
// that can be used as a callback to that function is known as using a named function in JavaScript. 

// Named functions are typically used when the same code needs to be executed multiple times throughout a program.

// EXAMPPLE
function namedFunc(){
    console.log('NAMED FUNCTION');    
}
namedFunc();
